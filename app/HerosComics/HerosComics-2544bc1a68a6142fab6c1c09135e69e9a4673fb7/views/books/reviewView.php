                <p>"배트맨 : 타임 앤 더 배트맨"는 3개의 다른 스토리로 구성이 되고 있습니다.</p>
                그러나 모두 연계가 되지는 않는데요. 첫 이야기는 "타임 앤 더 배트맨"으로 메이비 머신을 둘러싼 과거 (브루스 웨인 & 딕 그레이슨), 현재 (딕 그레이슨 & 데미안 웨인) 그리고 미래의 배트맨 (데미안 웨인)에 이어 더 미래의 배트맨의 이야기가 이어지게 됩니다. 이 스토리에서 그랜트 모리슨은 논라이너 형식을 차용하여, 카터 니콜라스 박스의 메이비 머신과 관련된 사건을 보여주게 됩니다. 이 방식은 여러 시대의 배트맨을 보여주는  영리한 방법이었다는 생각을 하게 됩니다.<br><br>
                두 번째 이야기는 브루스 웨인이 파이널 크라이시스로 가는 이야기인데요. 일종의 인비트윈퀄로 보시면 될 듯합니다. 서바이벌 리스트로서의 브루스 웨인을 부각 시키고 있습니다. 저스티스 리그의 호출을 받은 브루스 웨인은  다크사이드와 마주하게 됩니다. 그리고... 마지막 이야기는 딕 그레이슨과 데미안의 다이나믹 듀오가 도주 전문가를 뒤쫓게 되는데, 배트맨이 브루스 웨인이 아니라는 의심을 사게 되는 것으로 마무리되게 됩니다.<br><br>
                사실 비슷 비슷한 이야기라도 누가 하느냐에 따라 달라지게 되는 것을 다시 한번 느꼈고, 그랜트 모리슨이 이야기꾼이라는 것을 확신하게 만드는 스토리들이었습니다. 재미있네요. 개인적으로 워너 브라더스가 배트맨 영화를 위해 제프 스나이더와 그랜트 모리슨을 컨설턴트로 섭외하면 좋을 것 같다는 생각을 다시 한 번 더 해보게 만드는 코믹북이었던 것 같습니다.
